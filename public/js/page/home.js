			var $email = $frm.find('.login_frm-email');
			var $phone = $frm.find('.login_frm-phone');
			resetError();

			var Email = $("#Email").val();
			var nationalityCode = $("#nationalityCode").val();
			var Phone = $("#Phone").val();
			var Password = $("#Password").val();
			var rememberMee = $("#rememberMee").prop("checked");


			if (($email.is(":visible") && Email == "") ||
			    ($phone.is(":visible") && Phone == "") ||
			    ($phone.is(":visible") && nationalityCode == "0")) {
				if ($email.is(":visible") && Email == "") {
					$email.addClass('js-error').append(errorMsg("请输入Email 帐号"));
				}
				if ($phone.is(":visible") && (Phone == "" && nationalityCode == "0")) {
					// console.log("1")
					$phone.find(".nationality_code").addClass('error-highlight')
					$phone.addClass('js-error').append(errorMsg("请输入手机号码与国籍码"));
				}
				else if ($phone.is(":visible") && Phone == "") {
					// console.log("2")
					$phone.addClass('js-error').append(errorMsg("请输入手机号码"));
				}
				else if ($phone.is(":visible") && (nationalityCode == "0")) {
					// console.log("3")
					$(".nationality_code").addClass("error-highlight");
					$phone.addClass('js-error').append(errorMsg("请选择国籍码"));
					$phone.find(".pgfrm-input").addClass('js-notErr');
				}

				return false;
			}
			else if ($email.is(":visible") && !pgUI.validateEmail(Email)) {
				$(".login_frm-email").addClass('js-error').append(errorMsg("格式錯誤"));
				return false;
			}

			if (Password == "") {
				$(".login_frm-pw").addClass('js-error').append(errorMsg("请输入密码"));
				return false;
			}
			var data = {};
			data.Email = Email;
			data.nationalityCode = nationalityCode;
			data.Phone = Phone;
			data.Password = Password;
			data.rememberMee = rememberMee;

			$.ajax({
				// url: '/path/to/file',
				// type: 'POST',
				// dataType: 'default: Intelligent Guess (Other values: xml, json, script, or html)',
				data: data,
			})
